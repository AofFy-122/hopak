export async function getTranslation() {
    const cookieStore = await cookies()
    const lang = cookieStore.get('horganice_lang')?.value || 'en'

    const t = (key) => {
        return translations[lang]?.[key] || translations['en'][key] || key
    }

    return { t, lang }
}
